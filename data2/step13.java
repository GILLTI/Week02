package data2;

public class step13 {
		public static void main(String[] args) {
		
			int a = 10;
			int b = 20;
			
			System.out.println(a > b);	//a가 b보다 크다
			System.out.println(a <= b);	//a가 b보다 작거나 같을때
			System.out.println(a == b);	//a랑 b랑 같을 때
			System.out.println(a != b);	//a랑 b랑 다를 때
			
		}
}
