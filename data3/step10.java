package data3;


public class step10 {
	public static void main(String[] args) {
		
		int num = 7;
		
		switch(num) {
		case 1: 	//case값이 일치하는 것을 찾아 해당하는 문장을 실행
			System.out.println("num 은 1 입니다");
			break;
		case 7:
			System.out.println("num 은 7 입니다");
			break;
		default : 	//생략 가능
			System.out.println("num은 1도 7도 아닙니다");
		}
		
		
		
		
	}
}
